public class FirmaBorc {
	private String tarih;
	private String adi;
	private String odenekKonusu;
	private double odenecekTutar;
	private double odecekNetTutar;
	private double odenenNetTutar;
	private Double kalan;
	
	public String getTarih() {
		return tarih;
	}
	public void setTarih(String tarih) {
		this.tarih = tarih;
	}
	public String getAdi() {
		return adi;
	}
	public void setAdi(String adi) {
		this.adi = adi;
	}
	public String getOdenekKonusu() {
		return odenekKonusu;
	}
	public void setOdenekKonusu(String odenekKonusu) {
		this.odenekKonusu = odenekKonusu;
	}
	public double getOdenecekTutar() {
		return odenecekTutar;
	}
	public void setOdenecekTutar(double odenecekTutar) {
		this.odenecekTutar = odenecekTutar;
	}
	public double getOdecekNetTutar() {
		return odecekNetTutar;
	}
	public void setOdecekNetTutar(double odecekNetTutar) {
		this.odecekNetTutar = odecekNetTutar;
	}
	public double getOdenenNetTutar() {
		return odenenNetTutar;
	}
	public void setOdenenNetTutar(double odenenNetTutar) {
		this.odenenNetTutar = odenenNetTutar;
	}
	public Double getKalan() {
		return kalan;
	}
	public void setKalan(Double kalan) {
		this.kalan = kalan;
	}

}
