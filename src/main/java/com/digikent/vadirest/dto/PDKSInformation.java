package com.digikent.vadirest.dto;

public class PDKSInformation {
	
	private long id;
	private String adi;
	private String soyAdi;
	private String turu;
	private String durum;
	private String tarihSaat;
	private String islem;
	private String kurumDisiMi;
	private String tarih;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getAdi() {
		return adi;
	}
	public void setAdi(String adi) {
		this.adi = adi;
	}
	public String getSoyAdi() {
		return soyAdi;
	}
	public void setSoyAdi(String soyAdi) {
		this.soyAdi = soyAdi;
	}
	public String getTuru() {
		return turu;
	}
	public void setTuru(String turu) {
		this.turu = turu;
	}
	public String getDurum() {
		return durum;
	}
	public void setDurum(String durum) {
		this.durum = durum;
	}
	public String getTarihSaat() {
		return tarihSaat;
	}
	public void setTarihSaat(String tarihSaat) {
		this.tarihSaat = tarihSaat;
	}
	public String getIslem() {
		return islem;
	}
	public void setIslem(String islem) {
		this.islem = islem;
	}
	public String getKurumDisiMi() {
		return kurumDisiMi;
	}
	public void setKurumDisiMi(String kurumDisiMi) {
		this.kurumDisiMi = kurumDisiMi;
	}
	public String getTarih() {
		return tarih;
	}
	public void setTarih(String tarih) {
		this.tarih = tarih;
	}

}
