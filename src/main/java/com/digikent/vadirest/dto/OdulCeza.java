package com.digikent.vadirest.dto;

public class OdulCeza {
	
	private String turu;
	private String tarih;
	private String konusu;
	private String cezaTuru;
	
	public String getTuru() {
		return turu;
	}
	public void setTuru(String turu) {
		this.turu = turu;
	}
	public String getTarih() {
		return tarih;
	}
	public void setTarih(String tarih) {
		this.tarih = tarih;
	}
	public String getKonusu() {
		return konusu;
	}
	public void setKonusu(String konusu) {
		this.konusu = konusu;
	}
	public String getCezaTuru() {
		return cezaTuru;
	}
	public void setCezaTuru(String cezaTuru) {
		this.cezaTuru = cezaTuru;
	}

}
